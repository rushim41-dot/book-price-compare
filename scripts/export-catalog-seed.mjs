function buildSeedSql(seedData) {
  const bookSlugs = seedData.books.map((book) => book.slug);

  return [
    "-- Books2Buy curated catalog seed.",
    "-- Generated by npm run export:catalog-seed.",
    "-- Safe to rerun: canonical rows are upserted and generated joins/assets are refreshed for seeded books.",
    "",
    "begin;",
    "",
    insertValues(
      "categories",
      ["slug", "label", "description"],
      seedData.categories.map((category) => [
        category.slug,
        category.label,
        category.description,
      ]),
      "on conflict (slug) do update set label = excluded.label, description = excluded.description, updated_at = now()"
    ),
    "",
    insertValues(
      "collections",
      ["slug", "label", "description"],
      seedData.collections.map((collection) => [
        collection.slug,
        collection.label,
        collection.description,
      ]),
      "on conflict (slug) do update set label = excluded.label, description = excluded.description, updated_at = now()"
    ),
    "",
    insertValues(
      "books",
      [
        "catalog_id",
        "slug",
        "title",
        "description",
        "category_slug",
        "thumbnail_url",
        "cover_fallback_path",
        "publisher",
        "published_date_text",
        "isbn13",
        "isbn10",
        "language",
        "format",
        "pages",
        "data_quality_status",
      ],
      seedData.books.map((book) => [
        book.catalogId,
        book.slug,
        book.title,
        book.description,
        book.categorySlug,
        book.thumbnailUrl,
        book.coverFallbackPath,
        book.publisher,
        book.publishedDateText,
        book.isbn13,
        book.isbn10,
        book.language,
        book.format,
        book.pages,
        book.dataQualityStatus,
      ]),
      [
        "on conflict (slug) do update set",
        "catalog_id = excluded.catalog_id,",
        "title = excluded.title,",
        "description = excluded.description,",
        "category_slug = excluded.category_slug,",
        "thumbnail_url = excluded.thumbnail_url,",
        "cover_fallback_path = excluded.cover_fallback_path,",
        "publisher = excluded.publisher,",
        "published_date_text = excluded.published_date_text,",
        "isbn13 = excluded.isbn13,",
        "isbn10 = excluded.isbn10,",
        "language = excluded.language,",
        "format = excluded.format,",
        "pages = excluded.pages,",
        "data_quality_status = excluded.data_quality_status,",
        "updated_at = now()",
      ].join(" ")
    ),
    "",
    insertValues(
      "authors",
      ["name", "normalized_name"],
      seedData.authors.map((author) => [author.name, author.normalizedName]),
      "on conflict (normalized_name) do update set name = excluded.name"
    ),
    "",
    scopedDelete("book_authors", bookSlugs),
    insertSelectValues(
      "book_authors",
      ["book_id", "author_id", "author_order", "role"],
      seedData.bookAuthors.map((row) => [
        bookIdSelect(row.bookSlug),
        authorIdSelect(row.authorKey, seedData),
        row.authorOrder,
        row.role,
      ])
    ),
    "",
    scopedDelete("book_tags", bookSlugs),
    insertSelectValues(
      "book_tags",
      ["book_id", "tag"],
      seedData.bookTags.map((row) => [bookIdSelect(row.bookSlug), row.tag])
    ),
    "",
    scopedDelete("book_collections", bookSlugs),
    insertSelectValues(
      "book_collections",
      ["book_id", "collection_slug", "position"],
      seedData.bookCollections.map((row) => [
        bookIdSelect(row.bookSlug),
        row.collectionSlug,
        row.position,
      ])
    ),
    "",
    scopedDelete("external_book_identifiers", bookSlugs),
    insertSelectValues(
      "external_book_identifiers",
      ["book_id", "source", "external_id", "external_url", "confidence", "verified_at"],
      seedData.externalIdentifiers.map((row) => [
        bookIdSelect(row.bookSlug),
        row.source,
        row.externalId,
        row.externalUrl,
        row.confidence,
        rawSql("now()"),
      ])
    ),
    "",
    scopedDelete("cover_assets", bookSlugs),
    insertSelectValues(
      "cover_assets",
      ["book_id", "source", "image_url", "storage_path", "status", "is_primary", "reviewed_at"],
      seedData.coverAssets.map((row) => [
        bookIdSelect(row.bookSlug),
        row.source,
        row.imageUrl,
        row.storagePath,
        row.status,
        row.isPrimary,
        rawSql("now()"),
      ])
    ),
    "",
    insertSelectValues(
      "store_offers",
      [
        "book_id",
        "store",
        "affiliate_query",
        "outbound_url",
        "product_id",
        "link_type",
        "price_amount_inr",
        "price_display_allowed",
        "offer_summary",
        "delivery_text",
      ],
      seedData.storeOffers.map((row) => [
        bookIdSelect(row.bookSlug),
        row.store,
        row.affiliateQuery,
        row.outboundUrl,
        row.productId,
        row.linkType,
        row.priceAmountInr,
        row.priceDisplayAllowed,
        row.offerSummary,
        row.deliveryText,
      ]),
      [
        "on conflict (book_id, store) do update set",
        "affiliate_query = excluded.affiliate_query,",
        "outbound_url = excluded.outbound_url,",
        "product_id = excluded.product_id,",
        "link_type = excluded.link_type,",
        "price_amount_inr = excluded.price_amount_inr,",
        "price_display_allowed = excluded.price_display_allowed,",
        "offer_summary = excluded.offer_summary,",
        "delivery_text = excluded.delivery_text,",
        "updated_at = now()",
      ].join(" ")
    ),
    "",
    "commit;",
    "",
  ].join("\n");
}

function insertValues(tableName, columns, rows, conflictClause = "") {
  if (rows.length === 0) {
    return "";
  }

  return [
    `insert into ${tableName} (${columns.join(", ")}) values`,
    rows.map((row) => `  (${row.map(toSqlValue).join(", ")})`).join(",\n"),
    `${conflictClause};`,
  ].join("\n");
}

function insertSelectValues(tableName, columns, rows, conflictClause = "") {
  if (rows.length === 0) {
    return "";
  }

  return [
    `insert into ${tableName} (${columns.join(", ")}) values`,
    rows.map((row) => `  (${row.map(toSqlValue).join(", ")})`).join(",\n"),
    `${conflictClause};`,
  ].join("\n");
}

function scopedDelete(tableName, bookSlugs) {
  return [
    `delete from ${tableName}`,
    "where book_id in (",
    "  select id from books",
    `  where slug in (${bookSlugs.map(toSqlValue).join(", ")})`,
    ");",
  ].join("\n");
}

function bookIdSelect(slug) {
  return rawSql(`(select id from books where slug = ${toSqlValue(slug)})`);
}

function authorIdSelect(authorKey, seedData) {
  const author = seedData.authors.find((candidate) => candidate.key === authorKey);

  if (!author) {
    throw new Error(`Missing author seed for ${authorKey}`);
  }

  return rawSql(
    `(select id from authors where normalized_name = ${toSqlValue(author.normalizedName)})`
  );
}

function rawSql(value) {
  return { rawSql: value };
}

function registerTypeScriptRequireHook() {
  const originalResolveFilename = Module._resolveFilename;

  Module._resolveFilename = function resolveFilename(request, parent, isMain, options) {
    if (request.startsWith("@/")) {
      return originalResolveFilename.call(
        this,
        path.join(rootDir, request.slice(2)),
        parent,
        isMain,
        options
      );
    }

    return originalResolveFilename.call(this, request, parent, isMain, options);
  };

  Module._extensions[".ts"] = function compileTypeScript(module, filename) {
    const source = require("node:fs").readFileSync(filename, "utf8");
    const output = ts.transpileModule(source, {
      compilerOptions: {
        module: ts.ModuleKind.CommonJS,
        moduleResolution: ts.ModuleResolutionKind.NodeJs,
        target: ts.ScriptTarget.ES2022,
        esModuleInterop: true,
        jsx: ts.JsxEmit.ReactJSX,
      },
      fileName: filename,
    });

    module._compile(output.outputText, filename);
  };
}

function toSqlValue(value) {
  if (value && typeof value === "object" && "rawSql" in value) {
    return value.rawSql;
  }

  if (value === null || value === undefined) {
    return "null";
  }

  if (typeof value === "number") {
    return Number.isFinite(value) ? String(value) : "null";
  }

  if (typeof value === "boolean") {
    return value ? "true" : "false";
  }

  return `'${String(value).replaceAll("'", "''")}'`;
}
